package seedu.duke.lesson;

public class TeachingStaff {

    String name;
    String email;

    public void setName(String teacherName) {
        name = teacherName;
    }

    public void setEmail(String teacherEmail) {
        email = teacherEmail;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }
}
